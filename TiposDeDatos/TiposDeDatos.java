//Declaramos el paquete TiposDeDatos
package TiposDeDatos;
//Declaramos la clase TiposDeDatos
public class TiposDeDatos {
// Método principal
        public static void main(String[] args) {
        // Declaración de variables de diferentes tipos de datos        
        byte tipoByte = 10;
        short tipoShort = 20;
        int tipoInt = 10000;
        long tipoLong = 1000000L;
        //
        float tipoFloat = 3.14f;
        double tipoDouble = 3.14159265359;
        // Declaración de variables de tipo char y boolean
        char tipoChar = 'A';
        boolean tipoBoolean = true;

        // Mostrar los valores de las variables 
        System.out.println("byte: " + tipoByte);
        System.out.println("short: " + tipoShort);
        System.out.println("int: " + tipoInt);
        System.out.println("long: " + tipoLong);
        System.out.println("float: " + tipoFloat);
        System.out.println("double: " + tipoDouble);
        System.out.println("char: " + tipoChar);
        System.out.println("boolean: " + tipoBoolean);
}
     
     }
    
