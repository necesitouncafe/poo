package MinusAMayus;

public class MinusAMayus {
    public static void main(String[] args) {
        // Declaramos una variable char con una letra minúscula
        char letra = 'g';

        // Convertimos la letra a mayúscula usando el método toUpperCase()
        char mayuscula = Character.toUpperCase(letra);

        // Mostramos el resultado
        System.out.println("Letra minúscula: " + letra);
        System.out.println("Letra mayúscula: " + mayuscula);
    }
}
